/*
L'incrémentation et décrémentation
*/

//#######incrémentation#######
var nb1 = 1;
nb1 = nb1 + 1;

// Affichage dans la console
console.log(nb1);
//Ecriture simplifiée
nb1++;
//Affichage console
console.log(nb1);

//######Décrémentation#######
nb1 = nb1 - 1;

// Affichage dans la console
console.log(nb1);
//Ecriture simplifiée
nb1--;
//Affichage console
console.log(nb1);

//subtilité
nb1 = 1;
console.log("nb1++ = "+ nb1++);
console.log(nb1);

nb1 = 1;
console.log("++nb1 = "+ ++nb1);